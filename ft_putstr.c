#include	"ft_printf.h"

int	ft_putstr(char *s)
{
	int	len;

	len = 0;
	if (!s)
		s = "(null)";
	while (*s)
	{
		write(1, s, 1);
		s++;
		len++;
	}
	return (len);
}
